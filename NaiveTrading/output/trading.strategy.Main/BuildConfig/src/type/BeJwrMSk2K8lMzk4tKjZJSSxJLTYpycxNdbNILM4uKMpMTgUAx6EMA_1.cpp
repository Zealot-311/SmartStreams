TupleMappings* SELF::initMappings()
{
    instantiators_.insert(std::make_pair("tuple<rstring ticker,rstring date,rstring time,float64 askprice>",&initer));
    TupleMappings * tm = new TupleMappings();
#define MY_OFFSETOF(member, base) \
    ((uintptr_t)&reinterpret_cast<Self*>(base)->member) - (uintptr_t)base
   
    // initialize the mappings 
    
    {
        std::string s("ticker");
        TypeOffset t(MY_OFFSETOF(ticker_, tm), 
                     Meta::Type::typeOf<SPL::rstring >(), 
                     &typeid(SPL::rstring));
        addMapping(*tm, t, s, 0);
    }
    
    {
        std::string s("date");
        TypeOffset t(MY_OFFSETOF(date_, tm), 
                     Meta::Type::typeOf<SPL::rstring >(), 
                     &typeid(SPL::rstring));
        addMapping(*tm, t, s, 1);
    }
    
    {
        std::string s("time");
        TypeOffset t(MY_OFFSETOF(time_, tm), 
                     Meta::Type::typeOf<SPL::rstring >(), 
                     &typeid(SPL::rstring));
        addMapping(*tm, t, s, 2);
    }
    
    {
        std::string s("askprice");
        TypeOffset t(MY_OFFSETOF(askprice_, tm), 
                     Meta::Type::typeOf<SPL::float64 >(), 
                     &typeid(SPL::float64));
        addMapping(*tm, t, s, 3);
    }
    
    return tm;
}

